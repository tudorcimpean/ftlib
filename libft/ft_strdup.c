#include "libft.h"

char		*ft_strdup(const char *s1)
{
	char	*s2;
	int		i;
	int		j;

	i = ft_strlen(s1);
	j = 0;
	s2 = (char *)malloc(sizeof(char) * (i + 1));
	if (!s1)
		return (0);
	while (j < i)
	{
		s2[j] = s1[j];
		j++;
	}
	s2[j] = '\0';
	return (s2);
}
